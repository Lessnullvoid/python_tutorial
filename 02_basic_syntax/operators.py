# Operators in Python

# Arithmetic Operators
a = 10
b = 3

print("Arithmetic Operations:")
print(f"Addition: {a + b}")
print(f"Subtraction: {a - b}")
print(f"Multiplication: {a * b}")
print(f"Division: {a / b}")
print(f"Floor Division: {a // b}")
print(f"Modulus: {a % b}")
print(f"Exponentiation: {a ** b}")

# Comparison Operators
print("\nComparison Operations:")
print(f"Equal to: {a == b}")
print(f"Not equal to: {a != b}")
print(f"Greater than: {a > b}")
print(f"Less than: {a < b}")
print(f"Greater than or equal to: {a >= b}")
print(f"Less than or equal to: {a <= b}")

# Logical Operators
x = True
y = False

print("\nLogical Operations:")
print(f"AND: {x and y}")
print(f"OR: {x or y}")
print(f"NOT: {not x}")

# Assignment Operators
c = 5
print("\nAssignment Operations:")
print(f"Initial value: {c}")
c += 2  # c = c + 2
print(f"After += 2: {c}")
c -= 1  # c = c - 1
print(f"After -= 1: {c}")
c *= 3  # c = c * 3
print(f"After *= 3: {c}") 