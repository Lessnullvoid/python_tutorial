# Variables and Data Types in Python

# String variables
name = "John Doe"
email = 'john@example.com'
print(f"Name: {name}, Email: {email}")

# Numeric variables
age = 25                  # integer
height = 1.75            # float
complex_number = 3 + 4j  # complex
print(f"Age: {age}, Height: {height}m")

# Boolean variables
is_student = True
is_employed = False
print(f"Student: {is_student}, Employed: {is_employed}")

# Multiple assignment
x, y, z = 1, 2, 3
print(f"x: {x}, y: {y}, z: {z}")

# Type checking
print(f"Type of name: {type(name)}")
print(f"Type of age: {type(age)}")
print(f"Type of height: {type(height)}")
print(f"Type of is_student: {type(is_student)}") 